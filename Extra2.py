a = int(input('tasteaza prima cifra: '))
b = int(input('tasteaza a doua cifra: '))
ex1 = a + b
ex2 = a - b
ex3 = a * b
ex4 = a / b
print("{a}+{b}={ex1}".format(b=b, a=a, ex1=ex1))
print("{a}-{b}={ex2}".format(b=b, a=a, ex2=ex2))
print("{a}*{b}={ex3}".format(b=b, a=a, ex3=ex3))
print("{a}/{b}={ex4}".format(b=b, a=a, ex4=ex4))